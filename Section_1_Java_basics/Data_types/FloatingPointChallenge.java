package Section_1_Java_basics.Data_types;

public class FloatingPointChallenge {
  public static void main(String[] args) {
    double kg = 0.45359237d;
    double randPound = 130.23d;

    double result = randPound * kg;
    System.out.println(randPound + " pounds converted to kg : " + result);
  }
}
